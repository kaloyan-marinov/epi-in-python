"""
source for examples: EPI in Python + own examples
"""

import math

print()
print("abs(-34.5)")
print(abs(-34.5))  # 34.5

print()
print("math.ceil(2.17)")
print(math.ceil(2.17))  # 3

print()
print("math.floor(3.14)")
print(math.floor(3.14))  # 3

print()
print("min(2.17, 3.14)")
print(min(2.17, 3.14))  # 2.17

print()
print("max(2.17, 3.14)")
print(max(2.17, 3.14))  # 3.14

print()
print("pow(7, 2)")
print(pow(7, 2))  # 49

print()
print("7 ** 2")
print(7 ** 2)  # 49

print()
print("math.sqrt(225)")
print(math.sqrt(225))  # 15.0
